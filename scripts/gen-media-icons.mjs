// Regenerate src/assets/mediaIcons.ts — the base64 PNGs served at /icons/media/v1/<slug>.png
// and referenced as Slack attachment `footer_icon`s. Build-time only: reads Lucide SVGs, recolours
// the stroke to the footer grey, rasterises to a small transparent PNG, and emits a base64 map.
//
// Run: node scripts/gen-media-icons.mjs   (needs devDeps: lucide-static, @resvg/resvg-js)
//
// Slack renders footer_icon at 16x16 and does NOT accept SVG (GIF/JPEG/PNG/BMP only), so we ship
// PNG at 2x (32px) for retina crispness. Keep the slug list in sync with mediaTypeIconSlug()
// in src/lib/slack/icons.ts.

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import { Resvg } from "@resvg/resvg-js";

const HERE = dirname(fileURLToPath(import.meta.url));
const REPO = join(HERE, "..");
const LUCIDE = join(REPO, "node_modules", "lucide-static", "icons");
const OUT = join(REPO, "src", "assets", "mediaIcons.ts");

// Demure grey matching the footer text (DEFAULT_BRIEF_COLOR / --muted).
const STROKE = "#868e96";
const SIZE = 32; // 2x the 16px Slack render

// Vertical lift (glyph units, out of 24): transparent space added at the BOTTOM of the kept-square
// canvas so the glyph is anchored higher. Slack vertically-centres footer_icon in a fixed 16px slot,
// which makes a centred glyph read a touch low against the footer text; biasing it up realigns it.
// Higher LIFT = higher glyph but slightly smaller (the square grows while the glyph stays 24u).
// Purely visual — tune against the live Slack render.
const LIFT = 4;

// Each media-type slug maps 1:1 to a Lucide icon name.
const SLUGS = ["tv", "radio", "newspaper", "globe", "message-circle"];

function renderPngBase64(slug) {
  const svgRaw = readFileSync(join(LUCIDE, `${slug}.svg`), "utf8");
  const side = 24 + LIFT;
  const dx = LIFT / 2; // centre the 24u glyph horizontally on the widened square
  // Bake the stroke colour (resvg has no CSS context), grow the viewBox to a bigger square, drop the
  // fixed width/height so resvg sizes from the viewBox, and wrap the glyph in a top-anchored,
  // horizontally-centred group so the extra room falls at the bottom.
  const svg = svgRaw
    .replace(/currentColor/g, STROKE)
    .replace(/\swidth="24"/, "")
    .replace(/\sheight="24"/, "")
    .replace(/viewBox="0 0 24 24"/, `viewBox="0 0 ${side} ${side}"`)
    .replace(/(<svg[^>]*>)/, `$1<g transform="translate(${dx}, 0)">`)
    .replace(/<\/svg>/, "</g></svg>");
  const resvg = new Resvg(svg, { fitTo: { mode: "width", value: SIZE }, background: "rgba(0,0,0,0)" });
  return resvg.render().asPng().toString("base64");
}

const entries = SLUGS.map((slug) => [slug, renderPngBase64(slug)]);

const body = entries.map(([slug, b64]) => `  ${JSON.stringify(slug)}: ${JSON.stringify(b64)},`).join("\n");
const file = `// AUTO-GENERATED by scripts/gen-media-icons.mjs — do not edit by hand.
// Base64 PNGs (${SIZE}px, stroke ${STROKE}) for the footer media-type icons. Served by the
// GET /icons/media/v1/:name route and referenced as Slack attachment footer_icons.
export const MEDIA_ICON_PNG: Record<string, string> = {
${body}
};
`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, file);
console.log(`Wrote ${OUT} with ${entries.length} icons: ${SLUGS.join(", ")}`);
